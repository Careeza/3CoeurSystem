void            setGround(CS_GameScene *scene)
{
    CS_Assets   *assets;
    int         x;
    int         xMin;
    int         xMax;

    assets = scene->QueryAssets();
    scene->QueryBorne(xMin, xMax);
    x = xMin;
    xMax = xMax / (float)Tools->QueryWindowWidth() * 100;
    while (x < xMax)
    {
        assets->loadAsset(Ground01, 0, x, YGround01);
        x += 10;
    }
}